package com.nezukoRent.database;

public class QuartierData {
    private int id;
    private String name;

    public QuartierData(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

 
}
